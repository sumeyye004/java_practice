package week02;

public class PrintTriangle {

    public static void main(String[] args){

        System.out.println("     *");
        System.out.println("   *   *");
        System.out.println("  *     *");
        System.out.println(" *********");

        System.out.println();
        System.out.println("----------------------");

        System.out.println("     *\n   *   *\n  *     *\n *********");
    }
}
