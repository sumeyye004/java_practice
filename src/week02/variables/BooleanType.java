package week02.variables;

public class BooleanType {

    public static void main(String[] args) {

        boolean freeShipping = true;
        Object onSale = false;
        var has5Stars = true;

        System.out.println(freeShipping);
        System.out.println(onSale);
        System.out.println(has5Stars);

        var married = true;
        System.out.println(married);

        boolean javaIsFun = true;
        System.out.println(javaIsFun);

        javaIsFun = false;
        System.out.println(javaIsFun);

        var n = 12345;
        System.out.println(n);
    }
}
