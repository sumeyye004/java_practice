package week02.variables;

public class CharType {

    public static void main(String[] args) {

        char first = 'J';
        char second = 'a';
        char third = 118;
        char fourth = 97;

        System.out.print(first);
        System.out.print(second);
        System.out.print(third);
        System.out.println(fourth);

        char smiley = '\u263A';
        System.out.println(smiley);


    }
}
